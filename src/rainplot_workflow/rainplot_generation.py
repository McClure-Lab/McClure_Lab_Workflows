def parse_args():
    parser = argparse.ArgumentParser(
        description="Generate rain plots from a BrdU .bed file."
    )
    parser.add_argument(
        "input_file",
        help="Path to the BrdU .bed file generated by extract_brdu.py"
    )
    parser.add_argument(
        "-o", "--output_dir",
        required=False,
        default=None,
        help="Output directory for rain plots (default: output/rain_plot/)"
    )
    parser.add_argument(
        "--region_start",
        type=int,
        default=None,
        help="Start coordinate used for extraction (for output filename)"
    )
    parser.add_argument(
        "--region_end",
        type=int,
        default=None,
        help="End coordinate used for extraction (for output filename)"
    )
    parser.add_argument(
        "--rfb_dir",
        required=False,
        default=None,
        help="Path to the data/bed directory containing the RFB .bed file"
    )
    parser.add_argument(
        "--phase",
        required=False,
        default="Unknown",
        help="Phase label for the plot title (e.g., Mitosis or S Phase)"
    )
    parser.add_argument(
        "--output_manifest",
        required=False,
        default=None,
        help="Optional file to write the generated rain plot PNG paths to"
    )
    return parser.parse_args()


def load_rain_plot_input(input_file):
    """Converts the input .bed file into a pandas dataframe."""
    rain_plot_df = pd.read_csv(
        input_file,
        sep="\t",
        header=None,
        names=["chrom", "start", "end", "read_id", "mod_base", "mod_prob"],
    )
    return rain_plot_df


def get_output_dir(output_dir=None):
    """Determine the output directory for the rain plots."""
    if not output_dir:
        output_dir = os.path.join(os.getcwd(), "output", "rain_plot")
    os.makedirs(output_dir, exist_ok=True)
    return output_dir


