def load_rfb_positions(rfb_dir, region_start, region_end):
    """
    Load RFB motif start positions per read from the RFB .bed file.
    Returns a dict of {read_id: [rfb_start_positions]}.
    """
    if not rfb_dir or region_start is None or region_end is None:
        return {}

    rfb_file = os.path.join(
        rfb_dir,
        f"rfb_bases{region_start}_to_{region_end}.bed"
    )

    if not os.path.exists(rfb_file):
        print(f"[WARN] RFB file not found: {rfb_file}", flush=True)
        return {}

    rfb_df = pd.read_csv(
        rfb_file,
        sep="\t",
        header=None,
        names=["chrom", "start", "end", "read_id", "mod_base", "mod_prob"]
    )

    # Build dict of read_id -> list of RFB start positions
    rfb_positions = {}
    for _, row in rfb_df.iterrows():
        rid = row["read_id"]
        if rid not in rfb_positions:
            rfb_positions[rid] = []
        rfb_positions[rid].append(int(row["start"]))

    print(
        f"[INFO] {len(rfb_positions)} reads with RFB motif positions loaded from {rfb_file}.",
        flush=True
    )
    return rfb_positions


