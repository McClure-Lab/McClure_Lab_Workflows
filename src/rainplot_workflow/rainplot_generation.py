def plot_rainplots_per_read(
    input_file,
    output_dir=None,
    region_start=None,
    region_end=None,
    rfb_dir=None,
    phase="Unknown",
    output_manifest=None
):
    df = load_rain_plot_input(input_file)
    outdir = get_output_dir(output_dir)

    df["start"] = pd.to_numeric(df["start"], errors="coerce", downcast="integer")
    df["mod_prob"] = pd.to_numeric(df["mod_prob"], errors="coerce", downcast="float")
    df = df.dropna(subset=["read_id", "start", "mod_prob"])

    max_reads = 100
    show_scatter = True
    threshold = 0.5

    df["mod_base"] = df["mod_base"].astype(str).str.upper()

    t_window = 100
    t_step = 45

    # Load RFB positions per read
    rfb_positions = load_rfb_positions(rfb_dir, region_start, region_end)

    # UNCOMMENT TO ENABLE: Filter to only plot reads that contain RFB coords
    # df = filter_reads_with_rfb(df, rfb_positions)

    # Get all unique reads with no filtering
    all_read_ids = df["read_id"].unique()
    if len(all_read_ids) == 0:
        raise ValueError("No reads found in the input file.")

    # Randomly sample up to max_reads
    sample_size = min(max_reads, len(all_read_ids))
    sampled_reads = np.random.choice(all_read_ids, size=sample_size, replace=False)
    df = df[df["read_id"].isin(sampled_reads)]

    generated_paths = []

    for i, (rid, sub) in enumerate(df.groupby("read_id", sort=False), start=1):
        if i > max_reads:
            break

        sub = sub.sort_values("start", kind="mergesort")

        genbank_ids = sub["chrom"].astype(str).unique()
        if len(genbank_ids) == 1:
            chr_label = genbank_to_chr.get(genbank_ids[0], genbank_ids[0])
        else:
            chr_label = "mixed/" + ",".join(
                genbank_to_chr.get(g, g) for g in genbank_ids
            )

        read_start = sub["start"].min()
        x = (sub["start"].to_numpy() - read_start) / 1000.0
        y = sub["mod_prob"].to_numpy(dtype=float)
        bases = sub["mod_base"].to_numpy(dtype=object)

        if len(x) < 2:
            continue

        t_idx = np.flatnonzero(bases == "T")
        if t_idx.size < t_window:
            continue

        # Threshold: convert probabilities to binary (1 if >= threshold, else 0)
        y_binary = (y >= threshold).astype(float)

        edges = []
        y_vals = []

        for j in range(0, t_idx.size - t_window + 1, t_step):
            window_t_idx = t_idx[j:j + t_window]
            if len(window_t_idx) < t_window:
                continue

            left_i = int(window_t_idx[0])
            right_i = int(window_t_idx[-1])

            x_left = float(x[left_i])
            x_right = float(x[right_i])

            if x_right < x_left:
                continue

            if not edges:
                edges.append(x_left)
            edges.append(x_right)

            # Mean of binary values = fraction of T's called as BrdU in this window
            y_bin = float(np.mean(y_binary[window_t_idx]))
            y_vals.append(y_bin)

        edges = np.asarray(edges, dtype=float)
        y_vals = np.asarray(y_vals, dtype=float)

        if len(edges) < 2 or len(y_vals) < 1:
            continue

        edges = np.maximum.accumulate(edges)

        if len(edges) >= 2 and edges[-1] == edges[-2]:
            edges[-1] = edges[-1] + 1e-9

        if len(edges) != len(y_vals) + 1:
            continue

        fig, ax = plt.subplots(figsize=(16, 4))

        # Scatter uses raw probabilities
        if show_scatter:
            ax.scatter(x, y, s=1, color="black", alpha=0.3)

        # Staircase uses binary thresholded mean (fraction of BrdU per window)
        ax.stairs(y_vals, edges, linewidth=2, color="black", fill=False)

        # Draw red vertical line at each RFB motif start position for this read
        if rid in rfb_positions:
            for rfb_start in rfb_positions[rid]:
                rfb_x = (rfb_start - read_start) / 1000.0
                ax.axvline(
                    x=rfb_x,
                    color="red",
                    linewidth=1.5,
                    linestyle="--",
                    label="RFB"
                )

            # Only show one legend entry even if multiple RFB lines
            handles, labels = ax.get_legend_handles_labels()
            ax.legend([handles[0]], [labels[0]], loc="upper right")

        ax.set_ylim(0, 1)
        ax.set_xlabel("Position within read (kb)")
        ax.set_ylabel("BrdU incorporation rate (0–1)")
        ax.set_title(f"{phase} Rain Plot {rid} - Read {i}\nChromosome: {chr_label}")

        outpath = os.path.join(
            outdir,
            f"rainplot_chr{chr_label}_{region_start}_{region_end}_{i:03d}.png"
        )

        fig.tight_layout()
        fig.savefig(outpath, dpi=200)
        plt.close(fig)
        generated_paths.append(outpath)

    if output_manifest:
        with open(output_manifest, "w", encoding="utf-8") as handle:
            for path in generated_paths:
                handle.write(f"{path}\n")


if __name__ == "__main__":
    args = parse_args()
